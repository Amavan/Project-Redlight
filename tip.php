<p>Weight loss is a sign of many disorders. But when you experience it along with an increased appetite and continuous thirst, you may have a certain type of diabetes. This condition also causes frequent urination, and may cause blurring of your distance vision. Type 1 diabetes occurs when cells in the pancreas stop secreting insulin, the hormone responsible for transporting sugar in the blood to your body cells. Instead, when sugar increases in the blood, it will pass into the urine, pulling extra fluid with it. You will drink more to replenish your fluids, and continue to urinate a lot.  Changes in fluid and sugar levels often cause fluctuating blurry vision. The loss of sugar and water in your urine results in lost calories, dehydration and hence weight loss. Due to the lack of insulin, the body cannot use the sugar as a source of energy.

<p>Diabetes can be diagnosed by obtaining a fasting blood sugar test, which means you do not eat for at least eight hours before the test. A fasting blood sugar level of 126 mg/dl or higher suggests the diagnosis of diabetes.

<p>If you have diabetes, it is important to keep track of your blood sugar levels. You can use a glucometer with a test strip (available at pharmacies) to check the sugar in your blood. Also, your doctor can obtain a very helpful blood test, called hemoglobin A1c, to get an average assessment of your blood sugar level over a period of three months. This test is particularly valuable because the complications of diabetes are closely related to the long-term continuous control of blood sugar levels. Diagnosing this disease early is important so that treatment can be instituted to prevent complications. These include kidney disease, blindness, heart ailments, skin problems, and damage to blood vessels. Diabetes may also lead to the loss of toes, feet, or legs as a result of the buildup of fat deposits in the blood vessels, which blocks the supply of nourishment and oxygen to these specific areas of the body.

<p>Type 1 diabetes is treated with insulin injections. A balanced diet with limited carbohydrates and minimal sugar intake is recommended. In addition, you should follow a healthy exercise program tailored to your needs.

<p>Type 2 diabetes, on the other hand, is caused by a combination of insufficient insulin plus resistance to the action of insulin. Weight loss and exercise improve insulin action and are helpful. A special diet is also important, and sometimes medications are needed to control blood sugar levels.

<p>If you experience increased urination, appetite, and thirst, often with weight loss, seek medical evaluation to determine if you have diabetes or if there is another cause for these symptoms.

